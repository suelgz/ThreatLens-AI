@lru_cache(maxsize=256)
def _translate_with_library(text: str, target_language: str) -> str:
    try:
        from deep_translator import GoogleTranslator

        return GoogleTranslator(source="auto", target=target_language).translate(text)
    except Exception:
        return text


def translate_text(key: str, language: str = "en", **kwargs) -> str:
    language = "tr" if language == "tr" else "en"
    text = TRANSLATIONS.get(language, {}).get(key)
    if text is None:
        text = TRANSLATIONS["en"].get(key, key)
        if language != "en":
            text = _translate_with_library(text, language)
    if kwargs:
        return text.format(**kwargs)
    return text
